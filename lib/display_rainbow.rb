colors = ['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet']

def display_rainbow(color)
  puts "R: #{color[0]}\n"
  puts "O: #{color[1]}\n"
  puts "Y: #{color[2]}\n"
  puts "G: #{color[3]}\n"
  puts "B: #{color[4]}\n"
  puts "I: #{color[5]}\n"
  puts "V: #{color[6]}\n"

end

display_rainbow(colors)